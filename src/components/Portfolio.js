  const FiltersList = ["All", "Websites", "Flayers", "Business Cards"]

class Portfolio extends React.Component {
    constructor(props) {
        super(props);

        this.state = {
            filters: FiltersList,
            selected: "All",
            projects: ProjectList,
        };
        this.selectedProjects = this.selectedProjects.bind(this)
    }

    selectedProjects = (category) => {
        if (category === "All") {
            this.setState({ projects: ProjectList });
        } else {
            const filter = ProjectList.filter((element) => element.category === category)
            if (filter) {
                this.setState({ projects: filter })
            }
        }

        this.setState({ selected: category })
    }

    render () {
        return (
            <div className="main">
                <Toolbar 
                filters={this.state.filters} 
                selected={this.state.selected} 
                onSelectFilter={(filter) => {this.selectedProjects(filter)}}
                />
                <Projects projects={this.state.projects}/>
            </div>
        )
    }
}

export default Portfolio;